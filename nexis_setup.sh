printf  "  ${DIM}ram    ${RST}%-26s" "$MEM"
[[ -n "$GPU" ]] && printf "  ${DIM}vram  ${RST}%s\n" "$GPU" || echo ""
echo -e "  ${DIM}────────────────────────────────────────────────────${RST}\n"
exec socat - UNIX-CONNECT:"$SOCK"
CLI_EOF

        && echo -e "  ${GN}●${RST} ollama   online" \
        || echo -e "  ${RD}●${RST} ollama   offline"
      echo ""; exit 0;;
    --start)  sudo systemctl start nexis-daemon && echo -e "${GN}  started.${RST}" || echo -e "${RD}  failed.${RST}"; exit 0;;
